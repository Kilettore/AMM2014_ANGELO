<h2>Benvenuto su Computer Shop!</h2><br>
Computer Shop e' un sito di eCommerce che permette agli utenti di acquistare pc, tablet o smartphone.<br><br>

Strumenti con cui e' stato realizzato il sito:<br>
- HTML e CSS<br>
- Model View Controller<br>
- MySQL e PHP<br>
- Javascript e AJAX (presenti nel footer di ogni pagina)<br>
- Gestione di piu' utenti (sito base, utente loggato, admin)<br>
- Transazione (acquistare i prodotti contenuti nel carrello)<br><br>

Strumenti che il sito NON utilizza<br>
- Rewrite engine<br>
- CSS responsive<br>
- Admin con IMP Token<br>
- Gestione immagini per id prodotto<br><br>

Cliccando il pulsante qui presente e' possibile raggiungere il sito.<br>
