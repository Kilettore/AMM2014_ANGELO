<p class="info_text"><b><i><span style="font-size: 18px;">Computer Shop</span></i></b><br>
Sviluppato da Angelo Cadoni per il corso di Amministrazione di Sistema<br>
Computer Shop è un negozio online di computer che permette all' utente di svolgere le operazioni più comuni
di tutti i siti di e-Commerce.<br><br>

Al suo interno 3 tipi di utenti possono svolgere determinate operazioni:<br>
- Gli utenti potranno acquistare prodotti, gestire il proprio carrello e modificare i propri dati personali.<br>
- Gli amministratori del sito invece possono fare tutto quello che può fare un utente normale, 
in più hanno la possibilità di aggiungere, rimuovere o modificare i prodotti presenti nel database.<br>
- Se un utente invece non effettua il login, potrà comunque visionare i prodotti presenti nel database e nel caso utilizzare la ricerca.<br><br>

<b><i>Vi auguriamo una buona navigazione su Computer Shop!</i></b>
